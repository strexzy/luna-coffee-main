// Позиция корзины — контракт стора корзины (shared/store).
// id — уникальный ключ позиции: один товар с разными опциями конструктора
// должен давать разные позиции (мердж одинаковых — фаза 5).
export interface CartItem {
  id: string;
  productId: string;
  name: string;
  // Миниатюра товара (нужна в корзине и в составе заказа).
  imageUrl: string;
  // Цена за единицу с учётом выбранных опций конструктора.
  unitPrice: number;
  quantity: number;
  // Человекочитаемое описание опций («M, овсяное молоко, со льдом»).
  optionsLabel?: string;
}
